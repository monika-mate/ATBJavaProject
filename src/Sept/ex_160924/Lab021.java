package Sept.ex_160924;

public class Lab021 {
    public static void main(String[] args) {
        //Compound Assignment Operator
        // +=, -=, *=, /=
        int a =10;
         a += 20;  // a= a+20;
        System.out.println(a);
        a -= 30;
        System.out.println(a);
        a *= 10;
        System.out.println(a);
        a /= 10;
        System.out.println(a);


    }
}
