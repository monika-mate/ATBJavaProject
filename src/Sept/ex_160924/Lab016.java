package Sept.ex_160924;

public class Lab016 {
    public static void main(String[] args) {
        //Arithmetic Operators
        // + - * /
        int a = 10;
        int b = 5;
        System.out.println(a+b); // + => binary = 2 operands
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);  // it gives you Quotient

        // % modulus operator

        System.out.println(a%b);   // it gives you a reminder

    }
}
