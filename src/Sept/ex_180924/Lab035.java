package Sept.ex_180924;

public class Lab035 {

    public static void main(String[] args) {
      //  Increment(++) Decrement(--)
        // pre and post

        // pre increment ++operand
        int a =10;
        int b = ++a;  // a=a+1
        System.out.println(b);


        int c =20;
        System.out.println(++c);
        System.out.println(c);

        // Post increment

        int d = 30;
        System.out.println(d++);
        System.out.println(d);

    }
}
